package assignment1;
public class SwitchExample2 {
	public static void main(String[] args) {
		char ch='x';
	      switch(ch)
	      {
		 case 'a':
		   System.out.println("Case1 : a is the symbol ");
		   break;
		 case 'b':
		   System.out.println("Case2 : b is the symbol");
		   break;
		 case 'x':
		   System.out.println("Case3 : x is the symbol");
		   break;
		 case 'y':
	           System.out.println("Case4 : y is the symbol");
	           break;
		 default:
		   System.out.println("Default  ");
	      }
	}

}
